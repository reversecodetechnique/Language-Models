    print("Binning contexts...")
    bins = get_context_bins(bigram_counts)

    print("Running EM for adaptive interpolation...")
    weights = em_train_adaptive_weights(val_tokens, unigram, bigram, trigram, bins, bigram_counts)

    # Prediction test
